import java.util.Scanner;
public class Main {
//Reverse the String in java
    public static void main(String[] args) {
       String name = "hellow" ;
        for(int i = name.length()-1; i>=0 ; i--){
            System.out.println(name.charAt(i));
        }
        System.out.println(name);
    
    }
    

}
